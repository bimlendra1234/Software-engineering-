    private int getcomplev(String valueOf) {
        
        if(valueOf.equalsIgnoreCase("Simple")) return 0;
        else if(valueOf.equalsIgnoreCase("Average")) return 1;
        else return 2;
    }

    private int calcUFP(int no_of_inputs, int no_of_outputs, int no_of_files, int no_of_Inquiries, int no_of_externalinterfaces,
            int inputlevel, int outputlevel, int filelevel, int Inquirylevel, int externalinterfacelevel) {
        
        int UFP=no_of_inputs*COMPLEXITY_VALUE_MAT[0][inputlevel]+
                no_of_outputs*COMPLEXITY_VALUE_MAT[1][outputlevel]+
                no_of_Inquiries*COMPLEXITY_VALUE_MAT[2][Inquirylevel]+
                no_of_files*COMPLEXITY_VALUE_MAT[3][filelevel]+
                no_of_externalinterfaces*COMPLEXITY_VALUE_MAT[4][externalinterfacelevel] ;
        return UFP;
    }

    private int calcDI() {

        String CAV_field=complex_adjust_value.getText();
        String Adjustment_StringValues[]=CAV_field.split(" ");
        int Adjustment_Values[]= new int[14];
        for(int i=0;i<Adjustment_StringValues.length;i++){
          Adjustment_Values[i]=Integer.parseInt(Adjustment_StringValues[i].trim());
        }
        System.out.println("Tota DI: " + IntStream.of(Adjustment_Values).sum());
        return IntStream.of(Adjustment_Values).sum();
    }

