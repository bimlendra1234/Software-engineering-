    private void calculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateActionPerformed
        // TODO add your handling code here:
        int Noi=Integer.parseInt(inputs.getText());
        int Noo=Integer.parseInt(outputs.getText());
        int Noinq=Integer.parseInt(inquiries.getText());
        int Nof=Integer.parseInt(files.getText());
        int Noextinf=Integer.parseInt(interfaces.getText());
        //System.out.println(""+no_of_inputs+no_of_outputs+no_of_Inquiries+no_of_files+no_of_externalinterfaces);
        int inplev=getcomplev(String.valueOf(option_inputs.getSelectedItem()));
        int outlev=getcomplev(String.valueOf(option_outputs.getSelectedItem()));
        int Inqlev=getcomplev(String.valueOf(option_inquiries.getSelectedItem()));
        int filelev=getcomplev(String.valueOf(option_files.getSelectedItem()));
        int extinflev=getcomplev(String.valueOf(option_interfaces.getSelectedItem()));
        //System.out.println(""+inputlevel+outputlevel+Inquirylevel+filelevel+externalinterfacelevel);
        
        int udp=calcUFP(Noi,Noo,Nof,Noinq,Noextinf,
                inplev,outlev,filelev,Inqlev,extinflev);
        int di=calcDI();
        
        float caf=0.65f + 0.01f*di;
        float FP_val=(float)udp*caf;
        float newfp=(float)FP_val;
        result_UFP.setText("UPF = "+Integer.toString(udp));
        result_CAF.setText("CAF = "+Float.toString(caf));
        result_FP.setText("FP = "+Float.toString(newfp));
    }//GEN-LAST:event_calculateActionPerformed

